package Scanners;

public class Decrement {
    public static void main(String[] args) {

        int age = 5;//

        System.out.println(age); //5
        System.out.println( ++age); //6
        System.out.println( age++); //6
        System.out.println(age); //


        int book = 10;
        System.out.println(book++);
        System.out.println(book++);
        System.out.println(book);



     }
}
